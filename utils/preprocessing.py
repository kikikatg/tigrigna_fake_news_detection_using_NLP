def tokenize(text):
    return re.findall(r"[A-Za-z]+|[ሀ-፿]+", text)


def clean_tokens(tokens):
    return [w for w in tokens if w not in stopwords]


def preprocess(text):
    text = clean_text(text)
    text = normalize_tigrigna(text)
    tokens = tokenize(text)
    tokens = clean_tokens(tokens)
    return " ".join(tokens)
